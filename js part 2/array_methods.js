/*
Array Methods

Push: add to end
Pop: delete from end & return it
UNshift: add to start
shift: delete from start & return it

*/

let names = ["omakr", "om"];
console.log(names);
names.push("OMKarr")
console.log(names);
names.pop();
console.log(names);
names.unshift("pratik");
console.log(names);
names.shift();
console.log(names);



