/*
Slice
Returns a part of the original string as a new string

let str = "LLoveCoding";
*/

let str= "ILoveCoding"

let str1=str.slice(1);
console.log(str1);

let str2 = str.slice(0, 5);
console.log(str2);

let str3 = str.slice(1, 5);
console.log(str3);

let str4 = str.slice(-2);
