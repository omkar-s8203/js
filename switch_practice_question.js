let day = 5;

switch (day) {
    case 1:
        console.log("monday");
        break;

    case 2:
        console.log("tuesday");
        break;

    case 3:
        console.log("WED");
        break;

    case 4:
        console.log("THUS");
        break;

    case 5:
        console.log("FRI");
        break;

    case 6:
        console.log("SAT");
        break;

    case 7:
        console.log("SUN");
        break;

    

    default:
        break;
}